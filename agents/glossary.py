def detect_terms(text: str) -> List[Dict[str, str]]:
    """Scan text for glossary terms and return matches with definitions."""
    found = []
    text_lower = text.lower()

    for term, definition in FINANCE_GLOSSARY.items():
        # Use word boundary matching for accuracy
        pattern = r'\b' + re.escape(term.lower()) + r'\b'
        if re.search(pattern, text_lower):
            found.append({
                "term": term,
                "definition": definition,
            })

    # Sort by position of first occurrence
    found.sort(key=lambda x: text_lower.find(x["term"].lower()))
    return found
